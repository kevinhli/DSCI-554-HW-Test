export const LA_ZILLOW_NAME_ALIASES = {
  'Lakeview Terrace': 'Lake View Terrace',
  'Crenshaw': 'Baldwin Hills/Crenshaw',
  'South Carthay': 'Carthay',
};

export function normalizeNeighborhoodName(name) {
  return name
    ?.toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

export function canonicalNeighborhoodName(name) {
  return LA_ZILLOW_NAME_ALIASES[name] || name;
}

export function slugifyNeighborhood(name) {
  return normalizeNeighborhoodName(name);
}
